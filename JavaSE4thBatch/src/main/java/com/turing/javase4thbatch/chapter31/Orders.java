    void loadOderDetails()
    {
        int row = this.tblOrders.getSelectedRow();
        if(row != -1)
        {
            long orderId = (Long)this.tblOrders.getValueAt(row, 0);
            this.frmOrderDetails.show();
            this.frmOrderDetails.loadOderDetails(orderId);
        }
    }
