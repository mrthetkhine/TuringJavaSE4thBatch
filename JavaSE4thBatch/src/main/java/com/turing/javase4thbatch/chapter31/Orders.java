    public void reloadOrders()
    {
        List<Order> orders = this.orderServie.getAllOrder();
        DefaultTableModel tableModel = (DefaultTableModel)this.tblOrders.getModel();
        tableModel.setRowCount(0);
        
      
        for(Order order: orders)
        {
            Object row [] = new Object[3];
            row[0] = order.getId();
            row[1] = order.getOrderDate();
            row[2] = order.getUserId();
           
            tableModel.addRow(row);
        }
    }
